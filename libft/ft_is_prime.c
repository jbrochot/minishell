int		ft_is_prime(int nb)
{
	long int i;

	i = 0;
	if (nb == 2)
		return (1);
	if (nb % 2 == 0 || nb < 2)
		return (0);
	while (i * i <= nb)
		i++;
	while ((i > 2) && ((nb % i) != 0))
		i--;
	return (nb % i);
}
